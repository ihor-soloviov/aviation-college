async function main() {
    const payload = await getPayload({ config })

    let created = 0
    let updated = 0

    for (const course of COURSES) {
        const data = {
            title: course.title,
            slug: course.slug,
            code: course.code,
            level: course.level,
            forms: course.forms,
            category: course.category,
            order: course.order,
            excerpt: course.excerpt,
            about: course.about,
            whatYouLearn: course.whatYouLearn.map((item) => ({ item })),
            careers: course.careers.map((item) => ({ item })),
            duration: course.duration,
            admission: course.admission,
            funding: {
                budget: true,
                contract: true,
                note: 'Вартість навчання за контрактом уточнюйте у приймальній комісії.',
            },
        }

        const existing = await payload.find({
            collection: 'courses',
            where: { slug: { equals: course.slug } },
            limit: 1,
            depth: 0,
        })

        if (existing.docs[0]) {
            await payload.update({
                collection: 'courses',
                id: existing.docs[0].id,
                data,
            })
            updated++
            console.log(`↻ Оновлено: ${course.title} (${course.level})`)
        } else {
            await payload.create({ collection: 'courses', data })
            created++
            console.log(`✓ Створено: ${course.title} (${course.level})`)
        }
    }

    console.log(`\nГотово: створено ${created}, оновлено ${updated}, всього ${COURSES.length}`)
    process.exit(0)
}

main().catch((err) => {
    console.error('[seed-courses] failed:', err)
    process.exit(1)
})
